    card_id text not null,
    standard boolean,
    pioneer boolean,
    modern boolean,
    premodern boolean,
    legacy boolean,
    vintage boolean,
    commander boolean,
    pauper boolean,
    paupercommander boolean,
    penny boolean,
    historic boolean,
    predh boolean,
    future boolean,
    alchemy boolean,
    oathbreaker boolean,
    timeless boolean,
    gladiator boolean,
    standardbrawl boolean,
    duel boolean,
    oldschool boolean,
    explorer boolean,
    brawl boolean,
    foreign key (card_id) references card(id)
);

create index if not exists idx_card on card(id);
